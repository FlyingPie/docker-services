<div> </div>
<div>Java</div>
<div> </div>
<div><strong>Description</strong></div>
<p>The game features a level generator, randomly creating a level at runtime using predefined rooms. Navigating through the levels is done via console commands and each room may contain keys, weapons or enemies. When moving to another room, an original door opening sound is played. The game includes a dynamic difficulty system, where each level will become increasingly more difficult by add more enemies.</p>
<p>Each level included their original background music, as heard in the classic Doom 95. The sounds made by guns, enemies and doors are also taken from the original game.</p>
<p>Additionally, for educational purposes, a language system and optional cheats where added.</p>
