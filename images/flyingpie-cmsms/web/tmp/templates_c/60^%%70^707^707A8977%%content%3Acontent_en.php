<p><strong>Features</strong></p>
<ul>
<li>Randomized level generator</li>
<li>Original weapon, enemy and door sounds as found in Doom 95</li>
<li>Original music per level and score at end of each level</li>
<li>Dynamic difficulty system based on the current level</li>
<li>Language system</li>
</ul>
